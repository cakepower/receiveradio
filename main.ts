function doSomething (num: number) {
    if (num == zsky) {
        pins.servoWritePin(AnalogPin.P3, 150)
        pins.servoWritePin(AnalogPin.P4, 150)
        basic.pause(500)
        pins.servoWritePin(AnalogPin.P3, 90)
        pins.servoWritePin(AnalogPin.P4, 90)
    } else {
    	
    }
}
