let zsky = 0
let strip: neopixel.Strip = null
strip = neopixel.create(DigitalPin.P13, 25, NeoPixelMode.RGB)
huskylens.initI2c()
huskylens.initMode(protocolAlgorithm.ALGORITHM_TAG_RECOGNITION)
strip.setMatrixWidth(5)
strip.setBrightness(30)
zsky = 0
radio.setGroup(34)
